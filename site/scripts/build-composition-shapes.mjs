import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import createHb from "harfbuzzjs/hb.js";
import hbjs from "harfbuzzjs/hbjs.js";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const SITE_ROOT = path.resolve(__dirname, "..");
const OUT_FILE = path.join(
  SITE_ROOT,
  "src",
  "generated",
  "compositionShapes.ts",
);
const WASM_FILE = path.join(
  SITE_ROOT,
  "node_modules",
  "harfbuzzjs",
  "hb.wasm",
);

const JOBS = [
  {
    key: "jp-type",
    font: "public/_font-subsets/NotoSansKR-Subset.ttf",
    text: "書体",
    script: "Jpan",
    language: "ja",
    weights: [400, 465],
  },
  {
    key: "jp-design",
    font: "public/_font-subsets/NotoSansKR-Subset.ttf",
    text: "デザイン",
    script: "Jpan",
    language: "ja",
    weights: [400, 465],
  },
  {
    key: "latin-type",
    font: "public/_font-subsets/GenInterfaceKR-Regular-Subset.ttf",
    text: "Type",
    script: "Latn",
    language: "en",
    weights: [400],
  },
  {
    key: "latin-design",
    font: "public/_font-subsets/GenInterfaceKR-Regular-Subset.ttf",
    text: "Design",
    script: "Latn",
    language: "en",
    weights: [400],
  },
];

const round = (value) => Math.round(value * 1000) / 1000;

function createShapeForText(hb, fontData, { text, script, language }, weight) {
  const blob = hb.createBlob(new Uint8Array(fontData));
  const face = hb.createFace(blob, 0);
  const font = hb.createFont(face);

  try {
    font.setVariations({ wght: weight });

    const shapeWith = (features) => {
      const buffer = hb.createBuffer();
      try {
        buffer.addText(text);
        buffer.setDirection("ltr");
        buffer.setScript(script);
        buffer.setLanguage(language);
        hb.shape(font, buffer, features);
        return buffer.json();
      } finally {
        buffer.destroy();
      }
    };

    const noPaltRaw = shapeWith("");
    const withPaltRaw = shapeWith("palt");

    return {
      glyphs: noPaltRaw.map((glyph, index) => ({
        g: glyph.g,
        cl: glyph.cl,
        path: font.glyphToPath(glyph.g),
        noPalt: {
          ax: round(glyph.ax),
          ay: round(glyph.ay),
          dx: round(glyph.dx),
          dy: round(glyph.dy),
        },
        withPalt: {
          ax: round(withPaltRaw[index].ax),
          ay: round(withPaltRaw[index].ay),
          dx: round(withPaltRaw[index].dx),
          dy: round(withPaltRaw[index].dy),
        },
      })),
    };
  } finally {
    font.destroy();
    face.destroy();
    blob.destroy();
  }
}

async function main() {
  const wasmBinary = await readFile(WASM_FILE);
  const hb = hbjs(await createHb({ wasmBinary }));
  const fontCache = new Map();
  const data = {};

  for (const job of JOBS) {
    const fontPath = path.join(SITE_ROOT, job.font);
    let fontData = fontCache.get(fontPath);
    if (!fontData) {
      fontData = await readFile(fontPath);
      fontCache.set(fontPath, fontData);
    }

    data[job.key] = {
      codepoints: [...job.text].map((char) => char.codePointAt(0)),
      weights: Object.fromEntries(
        job.weights.map((weight) => [
          String(weight),
          createShapeForText(hb, fontData, job, weight),
        ]),
      ),
    };
  }

  const body = JSON.stringify(data, null, 2);
  await writeFile(
    OUT_FILE,
    `// This file is generated by site/scripts/build-composition-shapes.mjs.
// Do not edit by hand.

export type GlyphPosition = {
  readonly ax: number
  readonly ay: number
  readonly dx: number
  readonly dy: number
}

export type CompositionGlyph = {
  readonly g: number
  readonly cl: number
  readonly path: string
  readonly noPalt: GlyphPosition
  readonly withPalt: GlyphPosition
}

export type CompositionWeightShape = {
  readonly glyphs: readonly CompositionGlyph[]
}

export type CompositionShape = {
  readonly codepoints: readonly number[]
  readonly weights: Readonly<Record<string, CompositionWeightShape>>
}

export const compositionShapes = ${body} as const satisfies Readonly<Record<string, CompositionShape>>
`,
  );
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
